CREATE TABLE PRODUCTO(
NOMBRE VARCHAR(30) NOT NULL,
PRECIO DECIMAL(5,2) NOT NULL,
SECCION VARCHAR(1) NOT NULL,
PASILLO VARCHAR(2) NOT NULL,
ESPACIO VARCHAR(2) NOT NULL,
CANTIDAD_PRODUCTO INTEGER NOT NULL,
CONSTRAINT NOMBRE_PRODUCTO_PK PRIMARY KEY(NOMBRE));

CREATE TABLE CLIENTE(
NOMBRE VARCHAR(20) NOT NULL,
APELLIDOS VARCHAR(50) NOT NULL,
EMPRESA VARCHAR(75) NOT NULL,
DIRECCION VARCHAR(300) NOT NULL,
TELEFONO VARCHAR(9) NOT NULL,
CORREO VARCHAR(200) NOT NULL,
CONSTRAINT EMPRESA_PK PRIMARY KEY(EMPRESA));

CREATE TABLE PEDIDO(
ID_PEDIDO INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1),
FECHA_PEDIDO DATE NOT NULL,
EMPRESA VARCHAR(75) NOT NULL,
NOMBRE_PLANTA VARCHAR(30) NOT NULL,
CANTIDAD INTEGER NOT NULL,
CONSTRAINT ID_PEDIDO_PK PRIMARY KEY(ID_PEDIDO),
CONSTRAINT EMPRESA_FK FOREIGN KEY (EMPRESA) REFERENCES CLIENTE (EMPRESA),
CONSTRAINT N_PRODUCTO_FK FOREIGN KEY (NOMBRE_PLANTA) REFERENCES PRODUCTO (NOMBRE));